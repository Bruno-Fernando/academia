
public class PressaoArterial {
	
	private int PAD;
	private int PAS;
	
	public PressaoArterial(int PAD, int PAS){
		this.PAD = PAD;
		this.PAS = PAS;
	}

	public int getPAD() {
		return PAD;
	}

	public void setPAD(int PAD) {
		this.PAD = PAD;
	}

	public int getPAS() {
		return PAS;
	}

	public void setPAS(int PAS) {
		this.PAS = PAS;
	}
	
	

}
